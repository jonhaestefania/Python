# Here appears a new method --> title() 
# A method is an action that Python can perform.
# The dot (.) tells Python to make the method performs its function
# title() method changes every word on the string to title case
# this is suitable for proper names, countries, and other stuff.

name = "kurt cobain"
print(name.title())

# In this code snippet the both methods upper() and lower()
# performs convert case to string stored on the variable hero
# their functions are all appreciated on data storing.

hero = "matt murdock"
print(hero.upper())
print(hero.lower())

