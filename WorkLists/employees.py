"""
In this I used Python for loop to run through all items in my
lists.
"""

print("Print out each name in the employees list")
employees = ['alison', 'elizabeth', 'carolina', 'angelica', 'jessica']
print(f"Employees: {employees}")

print("\nEach name: ")
for employee in employees:
    print(employee.title())

